n=int(input())
if n<60:
    print("F")
elif n>=60 and n<=64:
    print("D")
elif n>=65 and n<=69:
    print("D+")
elif n>=70 and n<=74:
    print("C")
elif n>=75 and n<=79:
    print("C+")
elif n>=80 and n<=84:
    print("B")
elif n>=85 and n<=89:
    print("B+")
elif n>=90:
    print("A")